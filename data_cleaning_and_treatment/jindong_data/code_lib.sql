DROP TABLE IF EXISTS JDATA_LABEL_SKU_0304_TBL;
CREATE TABLE JDATA_LABEL_SKU_0304_TBL AS
  SELECT
    USER_ID
    , SKU_ID
    , MAX(CASE WHEN TYPE = 4 THEN 1 ELSE 0 END) AS Y
  FROM (
    SELECT
      *
    FROM  JDATA_ACTION_TBL
    WHERE
      1=1
      AND TIME >= DATE('2016-04-01')
      AND TIME <  DATE('2016-04-06')
      AND CATE = '8'
      AND TYPE = '4'
  )
  GROUP BY
    USER_ID
    , SKU_ID
;
